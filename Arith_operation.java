

public class Arith_operation {

    public static void main(String[] args) {
        int a=5,b=10;
        int sum = a+b;
        int substraction = a-b;
        int multiplication = a*b;
        int division = a/b;
        int sqrt = a*a;
        int cube_root = b*b*b;


        
        System.out.println("Addition of two numbers is :" +sum);
        System.out.println("Substraction of two numbers is :" +substraction);
        System.out.println("Multiplication of two numbers is :" +multiplication);
        System.out.println("Division of two numbers is :" +division);
        System.out.println("Square Root of a number is :" +sqrt);
        System.out.println("Cube Root of a number is :" +cube_root);
        System.out.println("Power of a number is :" +(5^3));

       
       
    }


    
}
